import { stopWordList } from "./stopWordAndAcronymsList/stopWordList";

export const removeStopWord = (docOrQuery) => {
  let words = docOrQuery.split(" ");
  words.forEach((word) => {
    stopWordList.indexOf(word) > 0
      ? (docOrQuery = docOrQuery.replace(new RegExp( word , "gi"), ""))
      : null;
  });

  return docOrQuery;
};



